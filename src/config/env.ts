// ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

const parseEnv = () => {
	const result = EnvSchema.safeParse(process.env);

	if (!result.success) {
		console.error("‚ùå –û—à–∏–±–∫–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ –æ–∫—Ä—É–∂–µ–Ω–∏—è:");
		console.error(result.error.flatten().fieldErrors);

		// –í production ‚Äî –ø–∞–¥–∞–µ–º —Å—Ä–∞–∑—É
		if (process.env.NODE_ENV === "production") {
			process.exit(1);
		}

		// –í development ‚Äî –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–∞–µ–º –∏ –ø—Ä–æ–±—É–µ–º fallback
		console.warn("‚ö†Ô∏è  –ò—Å–ø–æ–ª—å–∑—É—é—Ç—Å—è –∑–Ω–∞—á–µ–Ω–∏—è –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é –¥–ª—è dev-—Ä–µ–∂–∏–º–∞");

		// –ë–µ–∑–æ–ø–∞—Å–Ω—ã–π fallback —Å safeParse —á—Ç–æ–±—ã –∏–∑–±–µ–∂–∞—Ç—å –∏—Å–∫–ª—é—á–µ–Ω–∏–π –∏–∑ —Ç—Ä–∞–Ω—Å—Ñ–æ—Ä–º–µ—Ä–æ–≤
		const fallbackResult = EnvSchema.safeParse({
			...process.env,
			// Defaults –¥–ª—è –æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã—Ö –ø–æ–ª–µ–π
			DATABASE_URL:
				process.env.DATABASE_URL ??
				"postgresql://flags:flags@localhost:5432/feature_flags",
			// –û—á–∏—â–∞–µ–º –∑–∞—à–∏—Ñ—Ä–æ–≤–∞–Ω–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è –µ—Å–ª–∏ –Ω–µ—Ç –ø–∞—Ä–æ–ª—è
			API_KEYS: isEncrypted(process.env.API_KEYS ?? "")
				? undefined
				: process.env.API_KEYS,
		});

		if (!fallbackResult.success) {
			console.error("‚ùå Fallback –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è —Ç–∞–∫–∂–µ –Ω–µ–≤–∞–ª–∏–¥–Ω–∞:");
			console.error(fallbackResult.error.flatten().fieldErrors);
			process.exit(1);
		}

		return fallbackResult.data;
	}

	return result.data;
};

// –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ –∑–∞—à–∏—Ñ—Ä–æ–≤–∞–Ω–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π –±–µ–∑ –ø–∞—Ä–æ–ª—è
const checkEncryptedValues = () => {
	const encryptedVars: string[] = [];

	for (const [key, value] of Object.entries(process.env)) {
		if (value && isEncrypted(value)) {
			encryptedVars.push(key);
		}
	}

	if (encryptedVars.length > 0 && !ENCRYPTION_PASSWORD) {
		console.error(
			"‚ùå –ù–∞–π–¥–µ–Ω—ã –∑–∞—à–∏—Ñ—Ä–æ–≤–∞–Ω–Ω—ã–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ, –Ω–æ ENCRYPTION_PASSWORD –Ω–µ –∑–∞–¥–∞–Ω:",
		);
		console.error(`   ${encryptedVars.join(", ")}`);
		process.exit(1);
	}

	if (encryptedVars.length > 0) {
		console.log(`üîê –†–∞—Å—à–∏—Ñ—Ä–æ–≤–∞–Ω—ã –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ: ${encryptedVars.join(", ")}`);
	}
};

checkEncryptedValues();

